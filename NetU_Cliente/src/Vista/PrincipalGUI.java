     /**
     * El propósito del método es limpiar y eliminar los checkBoxes en el array
     * y las publicacions en el Panel Contenedor. Se utiliza cada vez que se usa
     * cargarPublicaciones()
     */
    public void eliminarPublicaciones() {
        checkBoxesPublicaciones.clear();
        panelContenedorPublicaciones.removeAll();
    }

    /**
     * El propósito del método es obtener el id de las publicaciones
     * seleccionadas para eliminarse.
     *
     * @return Una lista de enteros si existen publicaciones, null de lo
     * contrario
     */
    public List<Integer> obtenerPublicacionesSeleccionadas() {

        if (panelContenedorPublicaciones.getComponentCount() > 0) {
            List<Integer> idPublicaciones = new ArrayList();

            for (JCheckBox checkbox : checkBoxesPublicaciones) {

                if (checkbox.isSelected()) {
                    idPublicaciones.add(Integer.parseInt(checkbox.getName()));
                }

            }

            return idPublicaciones;
        } else {
            return null;
        }

    }

    public void desSeleccionarPublicaciones() {

        for (JCheckBox checkbox : checkBoxesPublicaciones) {

            if (checkbox.isSelected()) {
                checkbox.setSelected(false);
            }

        }

    }

    /**
     * El proposito del metodo es instanciar el Form EscribirPublicacion como un
     * Thread implementando Runneable
     *
     * @param escucha El controlador es quien escucharà
     */
    public void escribirPublicacion(ActionListener escucha) {

        Thread hiloEscrirPublicacion;
        escribirPublicacion = new EscribirPublicacion(this, escucha);
        hiloEscrirPublicacion = new Thread(escribirPublicacion);

        hiloEscrirPublicacion.start();

    }

    /**
     * El propósito del método es cargar y mostrar en el Form las publicaciones
     * del Empleado. Se utiliza cuando el empleado incia sesión, eliminar o crea
     * Publicaciones.
     *
     * @param publicaciones El array de PublicacionesPaquete del Empleado
     */
    public void cargarPublicaciones(Publicaciones publicaciones) {
        eliminarPublicaciones();
        List<Paquetes.Publicacion> listaPublicaciones;
        listaPublicaciones = publicaciones.obtenerPublicaciones();

        for (Paquetes.Publicacion publicacionPaquete : listaPublicaciones) {

            Publicacion publicacion = new Publicacion(publicacionPaquete.getId_Publicacion(),
                    publicacionPaquete.getFecha(), publicacionPaquete.getHora(),
                    publicacionPaquete.getContenido());

            GridBagConstraints restriccion = new GridBagConstraints();
            restriccion.gridx = 0;
            restriccion.gridy = panelContenedorPublicaciones.getComponentCount();
            restriccion.fill = GridBagConstraints.HORIZONTAL;
            restriccion.insets = new Insets(2, 0, 2, 0);
            restriccion.weightx = 1.0;
            restriccion.ipady = 100;
            checkBoxesPublicaciones.add(publicacion.getCheckBoxBorrar());
            panelContenedorPublicaciones.add(publicacion, restriccion);
        }

        panelContenedorPublicaciones.revalidate();
        panelContenedorPublicaciones.invalidate();
        repaint();

    }


    /**
     * El propósito del método es actualizar el Form para modificar la
     * descripcion
     *
     * @param tipo Si es 1, entonces inicia la modificacion de la descripcion.
     * Si es 2, entonces cancela/guarda la modificacion.
     */
    public void modificarDescripcion(int tipo) {

        switch (tipo) {
            case 1: {
                btnModificarDescripcion.setText("Guardar");
                txtAreaDescripcion.setEditable(true);
                txtAreaDescripcion.requestFocus(true);
                lblDescripcion.setText("Oprima ESC para cancelar");
            }
            break;
            case 2: {
                btnModificarDescripcion.setText("Editar descripción");
                txtAreaDescripcion.setEditable(false);
                lblDescripcion.setText("Descripción");
                txtAreaDescripcion.setText(descripcionEmpleado);
                panelPerfil.requestFocus();
            }
            break;

        }

        panelPerfil.updateUI();
    }

