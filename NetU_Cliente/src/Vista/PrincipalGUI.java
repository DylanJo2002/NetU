    public void desplegarMensajeDialogo(int tipo, String titulo, String mensaje) {

        Toolkit.getDefaultToolkit().beep();

        switch (tipo) {

            case 1: {
                JOptionPane.showMessageDialog(null, mensaje, titulo,
                        JOptionPane.PLAIN_MESSAGE, obtenerIcono("/imagenes/error_icon.png"));
            }
            break;

            case 2: {
                JOptionPane.showMessageDialog(null, mensaje, titulo,
                        JOptionPane.PLAIN_MESSAGE, obtenerIcono("/imagenes/exito_icon.png"));
            }

        }
    }

    public int desplegarMensajeConfirmacion(String titulo, String mensaje) {
        Toolkit.getDefaultToolkit().beep();

        int respuesta = 0;
        respuesta = JOptionPane.showConfirmDialog(null, mensaje, titulo,
                JOptionPane.YES_NO_OPTION, JOptionPane.PLAIN_MESSAGE,
                obtenerIcono("/imagenes/pregunta_icon.png"));

        return respuesta;

    }
    
